    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        int r=jTable2.getSelectedRow();
        if(r>=0 && r<jTable2.getRowCount()){
            String tenl=jTextField5.getText();
            int sosv=Integer.parseInt(jTextField6.getText());
            tmL.setValueAt(tenl, r, 1);
            tmL.setValueAt(sosv, r, 2);
            int ma=Integer.parseInt(jTextField4.getText());
            Lop l=new Lop(ma, tenl, sosv);
            listL.set(r, l);
        }
        else{
            JOptionPane.showMessageDialog(this, "Chon lop de sua");
            return;
        }
    }//GEN-LAST:event_jButton5ActionPerformed


    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int n=listP.size();
        if(n>0)
            Phong.setSma(listP.get(n-1).getMa()+1);
        jTextField1.setText(Phong.getSma()+"");
        String tenp,kieu;
        int somay;
        try {
            tenp=jTextField2.getText();
            somay=Integer.parseInt(jTextField3.getText());
            kieu=jTextField4.getText();
            Phong p=new Phong(tenp, somay, kieu);
            tmP.addRow(p.toObjects());
            listP.add(p);
        } catch (TrongException e) {
            JOptionPane.showMessageDialog(this, "khong de trong");
            return;
        } catch (NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Nhap vao so");
            return;
        }
    }//GEN-LAST:event_jButton1ActionPerformed


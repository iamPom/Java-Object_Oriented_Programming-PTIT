    private void loadP(){
        File f=new File(fP);
        if(f.exists()) listP= IOFile.doc(fP);
        else listP=new ArrayList<>();
    }
    private void loadL(){
        File f=new File(fL);
        if(f.exists()) listL= IOFile.doc(fL);
        else listL=new ArrayList<>();
    }
    private void showP(List<Phong> list){
        tmP.setRowCount(0);
        for(Phong i:list)
            tmP.addRow(i.toObjects());
    }
    private void showL(List<Lop> list){
        tmL.setRowCount(0);
        for(Lop i:list)
            tmL.addRow(i.toObjects());
    }
    

